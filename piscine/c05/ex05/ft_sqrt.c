int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0 || nb > 2147483647)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while (i * i != nb)
	{
		if (i == nb / 2)
		{
			return (0);
		}
		i++;
	}
	return (i);
}
