#include <unistd.h>

int	assign_size(void);

void	show(int a[9][9])
{
	int		i[2];
	int		size;

	size = assign_size();
	i[0] = -1;
	while (++i[0] < size)
	{
		i[1] = -1;
		while (++i[1] < size)
		{
			a[i[0]][i[1]] += 48;
			write(1, &a[i[0]][i[1]], 1);
			a[i[0]][i[1]] -= 48;
			if (i[1] != size - 1)
				write(1, " ", 1);
		}
		write(1, "\n", 1);
	}
}
