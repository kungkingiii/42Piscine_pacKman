#include <stdio.h>

char	*ft_convert(char *dest)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		if (dest[i] >= '0' && dest[i] <= '9')
		{
			dest[j] = dest[i];
			j++;
		}
		i++;
	}
	dest[j] = '\0';
	return (dest);
}
